package com.bean;

public class Grade {
    private int id;
    private String Cname;
    private String Gmark;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCname() {
        return Cname;
    }

    public void setCname(String cname) {
        Cname = cname;
    }

    public String getGmark() {
        return Gmark;
    }

    public void setGmark(String gmark) {
        Gmark = gmark;
    }
}
